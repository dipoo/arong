package org.arong.tags.functions;

public class Maths {
	/**
	 * 返回 int 值的绝对值。
	 * 
	 * @param a
	 * @return
	 */
	public static int abs(int a) {
		return Math.abs(a);
	}

	/**
	 * 返回 long 值的绝对值。
	 * 
	 * @param a
	 * @return
	 */
	public static long abs(long a) {
		return Math.abs(a);
	}

	/**
	 * 返回 float 值的绝对值。
	 * 
	 * @param a
	 * @return
	 */
	public static float abs(float a) {
		return Math.abs(a);
	}

	/**
	 * 返回 double 值的绝对值。
	 * 
	 * @param a
	 * @return
	 */
	public static double abs(double a) {
		return Math.abs(a);
	}

	/**
	 * 返回 double 值的立方根。
	 * 
	 * @param a
	 * @return
	 */
	public static double cbrt(double a) {
		return Math.cbrt(a);
	}

	/**
	 * 返回最小的（最接近负无穷大）double 值，该值大于等于参数，并等于某个整数。
	 * 
	 * @param a
	 * @return
	 */
	public static double ceil(double a) {
		return Math.ceil(a);
	}

	/**
	 * 返回最大的（最接近正无穷大）double 值，该值小于等于参数，并等于某个整数。
	 * 
	 * @param a
	 * @return
	 */
	public static double floor(double a) {
		return Math.floor(a);
	}

	/**
	 * 返回两个 double 值中较大的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static double max(double a, double b) {
		return Math.max(a, b);
	}

	/**
	 * 返回两个 float 值中较大的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static float max(float a, float b) {
		return Math.max(a, b);
	}

	/**
	 * 返回两个 int 值中较大的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static int max(int a, int b) {
		return Math.max(a, b);
	}

	/**
	 * 返回两个 long 值中较大的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static long max(long a, long b) {
		return Math.max(a, b);
	}

	/**
	 * 返回两个 long 值中较小的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static long min(long a, long b) {
		return Math.min(a, b);
	}

	/**
	 * 返回两个 int 值中较小的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static int min(int a, int b) {
		return Math.min(a, b);
	}

	/**
	 * 返回两个 float 值中较小的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static float min(float a, float b) {
		return Math.min(a, b);
	}

	/**
	 * 返回两个 double 值中较小的一个。
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public static double min(double a, double b) {
		return Math.min(a, b);
	}

	/**
	 * 返回带正号的 double 值，该值大于等于 0.0 且小于 1.0。
	 * 
	 * @return
	 */
	public static double random() {
		return Math.random();
	}

	/**
	 * 返回正确舍入的 double 值的正平方根。
	 * 
	 * @param a
	 * @return
	 */
	public static double sqrt(double a) {
		return Math.sqrt(a);
	}
}